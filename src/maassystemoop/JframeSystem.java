    private void EkleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EkleButtonActionPerformed
        // TODO add your handling code here:
        
Eleman e = new Eleman();
        
        String empname = txtE_adi.getText();
        int sal = Integer.parseInt(txte_Maas.getText());
        int phone  = Integer.parseInt(txte_Tel.getText());
        
        e.setEmpname(empname);
        e.setSalary(sal);
        e.setPhone(phone);
      
        ElemanIslemler dao = new ElemanIslemler();
        dao.save(e);
        Load();
        txtE_adi.setText("");
        txte_Maas.setText("");
        txte_Tel.setText("");
        txtE_adi.requestFocus();
    }//GEN-LAST:event_EkleButtonActionPerformed

