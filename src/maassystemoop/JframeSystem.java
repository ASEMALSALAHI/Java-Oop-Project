    public void Load()
    {
        ElemanIslemler dao = new ElemanIslemler();
        List<Eleman> list = dao.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Eleman st: list)
        {
            int sid = st.getId();
            String ElemanName = st.getEmpname();
            int maas = st.getSalary();
            int tel = st.getPhone();
            DFT.addRow(new Object[]{sid,ElemanName,maas,tel});
        }    
  
    }

